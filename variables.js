const a = 1;
// `const` creates a variable with block scope (only accessible within its block)
// Its value cannot be changed (immutable)

var b = 1;
// `var` creates a variable with global scope (accessible throughout the whole script)
// Its value can be changed (mutable)

let c = 1;
// `let` creates a variable with block scope (only accessible within its block)
// Its value can be changed (mutable)
