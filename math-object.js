console.log(Math.PI);
// Prints the value of PI (3.14159...)

console.log(Math.pow(2, 6));
// 2 raised to the power of 6 (same as 2**6)
// Output: 64

console.log(2 ** 6);
// The exponentiation operator (**) does the same thing as Math.pow
// Output: 64

console.log(`Square root of 50 is ${Math.sqrt(50)}`);
// Finds the square root of 50
// Output: 7.071...

console.log(Math.abs(-311));
// Absolute value (removes the negative sign)
// Output: 311

console.log(Math.ceil(5.1));
// Rounds 5.1 up to the next whole number
// Output: 6

console.log(Math.floor(7.99));
// Rounds 7.99 down to the previous whole number
// Output: 7

console.log(Math.min(1, 5, 7, 0, 12, 8));
// Finds the smallest number in the list
// Output: 0

console.log(Math.max(1, 5, 7, 0, 12, 8));
// Finds the largest number in the list
// Output: 12

console.log(Math.random());
// Generates a random decimal between 0 and 1

console.log(Math.random() * 20);
// Generates a random decimal between 0 and 20
